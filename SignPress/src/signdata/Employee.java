package signdata;

public class Employee 
{
    public int Id;			//  用户的ID


    public String Name;		//  员工的名字


    public String Position;		//  员工的职位信息


    public Department Department;		// 员工的部门信息



    public int CanSubmit;				//  是否可以提交


    public int CanSign;					//  是否可以签字


    public int IsAdmin;					//  是否是管理员
    public User User;					//  用户信登录信息

    public String SignatureUrl;			// 员工的签字信息路劲 

}
